def largest_product(integer_list, span=5):
    """ Returns max product of n consequtive integers, start index and end
    index.

    This problem boils down to maximum sub-array problem which is best solved
    by Kadane's Algorithm in O(n)
    """

    max_product, max_start_index, max_end_index = 0, 0, 0
    current_max = 0
    size = len(integer_list) - 1

    for i, v in enumerate(integer_list):
        if i + span <= size:
            current_max = reduce(mul, integer_list[i:i+span])
            if current_max > max_product:
                max_product, max_start_index, max_end_index = (current_max,
                                                               i,
                                                               i+span)
    return max_product, max_start_index, max_end_index


